import json
import argparse
import pandas as pd
import numpy as np
from collections import defaultdict

def main():
    parser = argparse.ArgumentParser(description="Statistics of Visual Grounding metrics by Category.")
    parser.add_argument("--input-json", type=str, default="/path/to/results/file.json", help="Path to the scored JSON file (output from previous step).")
    parser.add_argument("--output-csv", type=str, default=None, help="Path to save the statistics CSV.")
    args = parser.parse_args()

    if args.output_csv is None:
        args.output_csv = args.input_json.replace(".json", "_category_stats.csv")

    print(f"Loading data from: {args.input_json}")
    with open(args.input_json, 'r', encoding='utf-8') as f:
        data = json.load(f)

    # Handle different JSON structures (nested vs list)
    target_list = []
    if isinstance(data, list):
        target_list = data
    elif isinstance(data, dict):
        if "details" in data:
            if isinstance(data["details"], list):
                 target_list = data["details"] # Flat list in details
            elif "visual_grounding" in data["details"]:
                target_list = data["details"]["visual_grounding"] # Nested in visual_grounding
        elif "visual_grounding" in data:
            target_list = data["visual_grounding"]

    if not target_list:
        print("Error: No sample data found in input JSON.")
        return

    # Data Container: { "category_name": { "ious": [], "offsets": [] } }
    cat_stats = defaultdict(lambda: {"ious": [], "offsets": []})
    
    total_objects = 0
    skipped_objects = 0

    print(f"Processing {len(target_list)} samples...")

    for item in target_list:
        # Check if "object_metrics" exists (generated by the previous script)
        if "object_metrics" not in item:
            continue

        for obj in item["object_metrics"]:
            total_objects += 1
            
            # Normalize category name (lowercase, strip whitespace)
            cat_name = obj.get("category", "unknown")
            if not cat_name: 
                cat_name = "unknown"
            cat_name = str(cat_name).lower().strip()

            # Get metrics
            iou = obj.get("iou", 0.0)
            offset = obj.get("center_offset")

            # Collect IoU (always exists, default 0.0)
            cat_stats[cat_name]["ious"].append(iou)

            # Collect Offset (only if matched, i.e., not None)
            if offset is not None:
                cat_stats[cat_name]["offsets"].append(offset)
            else:
                # If offset is None (missed detection), you can choose to ignore or handle it.
                # Here we ignore it for Mean Offset calculation to avoid skewing with arbitrary penalties.
                pass

    # --- Aggregation ---
    records = []
    for cat, metrics in cat_stats.items():
        ious = metrics["ious"]
        offsets = metrics["offsets"]
        
        n_total = len(ious)
        n_matched = len(offsets) # Count of objects successfully grounded (offset is not None)

        if n_total == 0: continue

        # IoU Stats
        mean_iou = np.mean(ious)
        median_iou = np.median(ious)

        # Offset Stats
        if n_matched > 0:
            mean_offset = np.mean(offsets)
            median_offset = np.median(offsets)
        else:
            mean_offset = float('inf') # Set infinite if no matches
            median_offset = float('inf')

        records.append({
            "Category": cat,
            "Count": n_total,
            "Matched Count": n_matched,
            "Mean IoU": mean_iou,
            "Median IoU": median_iou,
            "Mean Offset (m)": mean_offset,
            "Median Offset (m)": median_offset
        })

    # Create DataFrame
    df = pd.DataFrame(records)

    # Sort by "Mean Offset (m)" ascending (smallest error first)
    # If offsets are equal, sort by "Mean IoU" descending (higher overlap first)
    if not df.empty:
        df = df.sort_values(by=["Mean Offset (m)", "Mean IoU"], ascending=[True, False])

    # Save
    df.to_csv(args.output_csv, index=False, float_format="%.4f")
    
    print("-" * 40)
    print(f"Total Categories: {len(df)}")
    print(f"Total Objects:    {total_objects}")
    print(f"Results saved to: {args.output_csv}")
    
    # Print Top 10 for preview
    print("\nTop 10 Categories (Best Performance / Lowest Offset):")
    print(df.head(10).to_string(index=False))

if __name__ == "__main__":
    main()